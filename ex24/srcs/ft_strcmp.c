#include "../includes/ft.h"

int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	if ((s1[i] != '\0') || (s2[i] != '\0'))
	{
		while (i < ft_strlen(s1))
		{
			if (s1[i] != s2[i])
			{
				return (s1[i] - s2[i]);
			}
			i = i + 1;
		}
	}
	return (0);
}
